int	ft_wordcount(char *s, int sep)
{
	int	wc;

	wc = 0;
	while (*s)
	{
		while (*s == (char)sep)
			s++;
		if (!*s)
			break ;
		while (*s != (char)sep && *s)
			s++;
		wc++;
	}
	return (wc);
}
